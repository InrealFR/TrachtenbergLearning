    public EnduranceMode(int mode, int minDigits, int maxDigits) {
        ActionListener compte_a_rebours ;
        UserAnswers = new ArrayList<>();
        RealAnswers = new ArrayList<>();
        this.minDigits = minDigits;
        this.maxDigits = maxDigits;
        initComponents();
        countdown = SelectionMode(mode);
        Nombre1 = RandomNumber(minDigits, maxDigits);
        Nombre2 = RandomNumber(minDigits, maxDigits);
        jTextAreaCalculs.setText(Nombre1 + " * " + Nombre2);
        RealAnswers.add(Nombre1 * Nombre2);
        Countdown CompteARebours = new Countdown(countdown);
        compte_a_rebours = new ActionListener(){
        public void actionPerformed(ActionEvent e){
            if(CompteARebours.getSecondes() == 0){
                CompteARebours.setMinutes(CompteARebours.getMinutes()-1);
                CompteARebours.setSecondes(59);
            } else {
                CompteARebours.setSecondes(CompteARebours.getSecondes()-1);
            }
            jLabelCountdown.setText(CompteARebours.getMinutes()+ " : "+CompteARebours.getSecondes());
        }
        };
        Countdown = new Timer(1000,compte_a_rebours);
        Countdown.start();
    }
