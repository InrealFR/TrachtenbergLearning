    private static int SelectionMode(int mode) {
        int ans = 0;
        switch (mode) {
            case 0: {
                ans = 1;
            }
            break;
            case 1: {
                ans = 2;
            }
            break;
            case 2: {
                ans = 5;
            }
            break;
            case 3: {
                ans = 10;
            }
            break;
        }
        return (ans);
    }

    private static int RandomNumber(int min, int max) {
        int ans = -1;
        int digits;
        System.out.println("MIN =" + min + " MAX=" + max);

        if (min == max) {
            digits = min;
        } else {
            digits = (int) ((Math.random() * (max - min)) + min);
        }
        while ((ans < Math.pow(10, min)) || (ans > Math.pow(10, max))) {
            ans = (int) (Math.random() * Math.pow(10, digits));
            System.out.println("ligne 220" + ans);
        }
        System.out.println("REPONSE =" + ans);
        return (ans);
    }
