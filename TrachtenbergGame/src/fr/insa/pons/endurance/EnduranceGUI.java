public static int UnDigitFinder(int index){
    int ans ;
 switch(index){
     case 0:{
         ans = 2 ;
     } break;
     case 1:{
         ans=3;
     }break;
     case 2:{
       ans=4;
     }break;
     case 3:{
         ans = 5 ;
     }break;
     case 4:{
         ans = 6 ;
     }break ;
     case 5:{
         ans = 7 ;
     }break ;
     case 6 : {
         ans = 8 ;
     }break ;
     case 7 : {
         ans = 9 ;
     } break ;
     case 8 : {
         ans = 11 ;
     } break ;
     case 9 : {
         ans = 12 ;
     } break ;
     default :{
         ans = 1 ;
     } break ;
 }
 return(ans);
}
